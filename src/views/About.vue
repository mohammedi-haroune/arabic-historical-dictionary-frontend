    المفسرون أنه الفرس، و أن الفرس ولده، و لعله حامر بالعربية أو بلغة أخرى.
    فأما هشام بن محمد فقال: ولد فارسور بن سام بن نوح عليه السلام بيرس به
    سميت فارس. و قال آخرون: الفرس ولد فارص بن يهوذا بن يعقوب عليه السلام
    فأما جرير بن عطية فإنه نسب الفرس و الروم إلى إسحاق فقال:

    و أبناء إسحاق الليوث إذا ارتدوا ... محامل موت لابسين السنورا

    إذا أفتخروا عدواً الصهبذ منهم و كسرى و آل الهرمزان و قيصرا و الله أعلم
    بالصحيح.
    فكان قباذ لما ملك ضعيفاً فوثبت ربيعة على عامله بالحيرة، و هو
    النعمان بن الأسود بن المنذر بن النعمان بن امرئ القيس بن عمرو بن امرئ
    القيس بن عمرو بن عديّ، فخرج منهم هارباً حتى نزل بأياد، فمات فيهم، و كان
    معه المنذر بن ماء السماء، و هو أقرب منه في قُعدُدِ النسب، و سنذكر نسب
    أمه و خبرها بعد هذا الفصل إن شاء الله و كان مع المنذر بن ماء السماء
    ابنه عمرو بن هند.

    و انطلقت ربيعة إلى كندة و كان الناس في الأول يقولون: إن كندة من ربيعة
    على ما قدمنا ذكره فجاءوا بالحارث بن عمرو بن حجر آكل المرار فملكوه على
    بكر بن وائل، و حشدوا له، و قاتلوا معه، فظهر على ما كانت العرب تسكن من
    أرض العراق، و أبى قباذ لضعفه أن يمد المنذر بجيش فكتب إلى الحارث بن عمرو
    إنني في غير قومي، و أنت أحق من كنفي فحوله إليه، و زوجه ابنته هند بنت
    الحارث فولدت له عمراً المذكور، و منذراً في قول هانئ بن المنذر البهيلي
    النسابة و قابوساً في قول غيره.

    فلما هلك قباذ و ملك ابنه كسرى و هو أنو شروان ملك المنذر بن ماء السماء
    على الحيرة، فأساء مكافأة بني آكل المرار، و طلبهم و سفك دماءهم، و جرى من
    ذلك ما ذكرناه في موضعه.

    و المنذر بن ماء السماء هو الذي قتله الحارث بن أبي شمر الغساني، و جرى من
    أولاد الحارث أعمام امرئ القيس ما سنذكره في موضعه إن شاء الله.

    و ملكت بنو أسد عليها حجر بن الحارث بن عمرو، و قيل: إن أباه كان ملكه
    عليها و هو أشبه، و تمليك ربيعة للحارث بن عمرو عليها قول خراش بن إسماعيل
    العجلي، و غيره يقول: إن قباذ هو الذي ملك الحارث بن عمرو على العرب، و
    ليس ذلك بثبت.

