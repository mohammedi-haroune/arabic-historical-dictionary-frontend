    المزن.

    و ثالثها: أنهم لاستغنائهم بأنفسهم. و اكتفائهم دون الحاجة إلى غيرهم و
    تحيزهم بتدبير أموالهم عن أكثر أغراض سواهم سموا بذلك، لأن الماء يوصف
    بالغنى عن الأشياء و بحاجتها إليه كما قال سهل بن هارون بن راهبون البليغ
    - رحمه الله، في بعض حكمه، يصف بعض الملوك في جلالة قدره، و استغنائه عن
    آراء اتباعه و عن مساعي أعوانه - : كالماء الذي ليس به إلى شيءٍ حاجة، و
    لكل شيء إليه أمس الضرورة.

    و رابعها: أنهم لما أحيوه من الأرض الميتة، و أقاموه من الأعلام الدارسة،
    و أهلوه من القفار العازبة، سموا بذلك، تشبيهاً بالماء الذي به حياة كل
    شيء قال الله عز و جل: (و جعلنا من الماءِ كلَّ شيءٍ حيٍّ).

    و القول التاسع: أنه مشتق من العرب، و هو النشاط قال النابغة الذبياني:

    و الخيل تمزع عرباً في أعنتها ... كالطير تنجو من الشؤبوب ذي البردِ
    و قد رواه الناس قاطبة بالغين معجمة إلا أن الخليل أورده في باب
    العين، وقد نوزع فيه، و وجه هذا الاشتقاق أنهم سموا بذلك لنشاطهم إلى
    إحراز المآثر، و تسرعهم إلى معونة الثائر و إجابة دعاء الجار المجرور، و
    اشتياقهم إلى اغتنام الثناء، و إدراك مناقب الكرماء، كما قال شاعرهم:

    و مستنبحٍ قال الصدى مثل قوله ... حضأتُ له ناراً لها حطبٌ جزلُ

    و قمت إليه مسرعاً فغنمته ... مخافة قومي أن يفوزوا به قبلُ

    و القول العاشر: أنه مشق من العرب و هو يبيس البهمى، و أحدتها عربة و قال
    أبو زياد - و هو يزيد بن عبد الله بن الحارث بن همام بن دهر بن ربيعة بن
    عمرو بن نفاثة بن عبد الله بن كلاب بن ربيعة بن عامر بن صعصعة - إن البهمى
    خير أحرار البقل رطباً و يابساً.

    و وجه هذا الاشتقاق أنهم سموا بذلك لأنهم من البشر بمنزلة البهمى من
    البقل. و يكون فيه وجه آخر أقوى من هذا و هو أن يبيس البهمى هو سفاها، و
    العرب تضرب به المثل في حدة شوكه، و تذلق غربه، حتى أنهم يسمونه نصالاً،
    قال القحيف العقيلي:

    على كل ذيال أطار نسيله ... عياب الحيا و الخصب حتى تفيلا

    رعى الروض و القربان حتى إذا رأى ... نصال السفا من حيث ركبن نصلا

    و قال الآخر، و أنشده ابن الأعرابي: إذا استنصل الهيف السفا برحت به
    عراقية الأقياظ نجد المرابع فيكونون سموا بذلك لحدة شجاعتهم، و نفوذ
    عزائمهم، و قد قيل في العرب إنه بالغينمعجمة، و العين أثبت.

    و القول الحادي عشر: أنه مأخوذ من التعريب، و هو الجبه بالغلط و الرد، و
    منه قول عمر: ما لكم إذا رأيتم الرجل يخرق أعراض الناس أن لا تعربوا عليه؟
    فقالوا: نخاف لسانه. قال ذلك أدنى أن لا تكونوا شهداء، رواه أبو عبيد و
    ابن الأعرابي و غيرهما. و معنى تعربوا عليه أي تردونه، و تدفعون قوله. و
    قال أوس بن حجر:

    ومثل ابن عثم إن ذحول تذكرت ... و قتلي تياس عن صلاح تعربُ

    ابن عثم: أحد بني جشم بن سعد. و تياس: أرض التقت فيها بنو سعد و بنو عمرو
    و كانت المعلاة لبني عمرو، قوم أوس يقول: فمثل هذه القتلى يمنع تذكره من
    الصلح.

    و يكون وجه هذا الاشتقاق أنهم سموا بذلك لأنهم يردون حكومة الظالم، و
    يعصون أمر الغاشم.

    و القول الثاني عشر: أنه مشتق من العبة، و هو النهر الشديد الجري، عن ابن
    دريد. و وجه هذا الاشتقاق أنهم شبهوا بالماء الجاري في قوة مسيله. و
    أعتياص رده و جره ما وجد في جريته.

    فأما العربة لهذه المنصوبات على دجلة و الفرات فمولد، إلا أن ثعلبة قد
    ذكره و صححه و قال: سميت بذلك من العربة و هو الشديد الجري من الأنهار، و
    لذلك لسرعة مدارها و اتصال جريانها.

    و القول الثالث عشر: أنه مشتق من التعريب، و هو مداواة للخيل بالنار، تسمى
    التبزيغ، و سميت العرب من ذلك لبلوغهم في شفاء الصدور بدرك الثأر، و إحكام
    ما عقدوه من عهد و ذمام مبلغ الكي الذي هو آخر الأدوية و أصعبها.

    و الأعراب جمع العرب. كالأعزاب جمع العزب، و لكن الشعراء استعملته بعد ذلك
    على اللفيف و سواد القبائل، ألا ترى إلى قول مكيث بن معاوية الكلبي - و
    قيل: مكيث بالضم - .

    و ما أسل الأعراب أرجو به الغنا ... و لو سلبت مالي سنون سوالبُ

    و قول الأشهب العكلي:

    يسموننا الأعراب و العرب اسمنا ... و نحن نسميهم رقاب المزاودِ

    و قال أبو فرعون السائل العدوي من عدي بن عبد مناة بن أد بن طابخة - واسمه
    شاكر ابن :

    و لست بسائل الأعراب شيئاً ... حمدت الله إذ لم يأكلوني

    و قد كنا ذكرنا مصدر عربي، فأما أعرابي فمصدره الأعرابية، قال بعض الشعراء:

    و إني لأهذي بالأوانس كالدمى ... و إني بأطراف القنا للعوبُ

    و إني على ما كان من أجنبيتي ... و لوثة أعرابيتي لأديبُ

    و أذكرني هذان البيتان بيتين لأبي الشغب العبسي:

    لعمرك إني يوم راح ابن كوكب ... لصب و إني للهوى لغلوبُ

    و إني على بعض الأناة و رسلتي ... لأبعد ما يرجو الفتى لطلوبُ

    فصل في اشتقاق اللغة
    رأيناه كالتوام لما قدمناه من اشتقاق العرب. في الحاجة إليه، و
    في النيابة عن أبي بكر رحمه الله بإيراده، و كثير ما يجري التساؤل بين أهل
    العلم عن هذه الكلمة و ما رأيت فيها لأحد من العلماء المتقدمين و لا
    المتأخرين قولاً شافياً و الله الموفق.

    قال الحسين بن علي: لسنا نشك في أن المقصد باسم العرب معنى واحد من
    المعاني التي ذكرناها، و كذلك اللغة، لأن واضع الكلمة إنما يقصد بها
    الإخبار عن المعنى. فإن استردفت معنى آخر كان رجحاناً، و لكن لما كان
    الحقيقة المقصد خافياً عنا كنا مضطرين إلى حشد الأقوال لتطيب أنفسنا بأن
    الصواب في جملة واحدة مما أوردناه، و على هذا الاعتبار ففي اشتقاق اللغة
    سبعة أقوال: فالقول الأول: أنها من لغيت بالشيء الغي إذا أولعت و أغريت
    به. قال الفراء: يقال لغيت بالكلام أقوله، و لغي بالماء يشربه، إذا أولع
    به، وردده، و لغيت بمصاحبة فلان إذا لهجت به. و قال أبن الأعرابي أيضاً:
    لغى به و لكى به إذا لهج به. و قال أبو عبيد في " غريب المصنف " . عن
    الكسائي: لغيت بالماء ألغى إذا لزمت شربه، أو نحوا من هذا اللفظ.

    و في وزنهما قولان أحدهما أن أصلها كانت لُغية عن وزن فُعلة، فحذفت الياء
    تخفيفاً و بنيت الكلمة على النقص مع نظرائها جماعة الألفاظ، فإن قال قائل:
    فما بالهم لم يحذفوا الياء من دجية واحدة الدجى، و حكمه حكم لغيه؟ فجوابه:
    أن القياس إنما يطرد في الأصول و الفروع المطرود عليها، فأما الحذف فإنما
    هو تخفيف، و فاعل التخفيف بالخيار فيه، إن شاء حذف و إن شاء أقر، و ذلك
    على حسب المواقع في كثرة الاستعمال العاذرة في طلب الاختصار. و القول
    الآخر أن وزنها فعلة، و أصلها لغية، فانقلبت الياء ألفاً لتحريكها، و
    انفتاح ما قبلها، و إنما كان الحكم حرفي المد و اللين أن ينقلب كل واحد
    منهما ألفاً إذا تحرك و انفتح ما قبله، لأن أخف أحواله أن يسكن، و ما قبله
    منه، أما سكونه فلان به تمكن المد فيه، و إما كون ما قبله منه فليلاحمه و
    يعينه و لا ينافره فما يراد به من مده و لينه و أوسط أحواله أن يسكن، ما
    قبله ليس منه، و أثقل أحواله أن يتحرك و ما قبله ليس منه، فيجري مجرى
    الحروف الصحاح، و تفارق صفته المطلوبة فيه بحركته التي تزيله عن اللبن. و
    بقلة مساعدة ما قبله له، و مشاكلته إياه، فإذا جاء حرف المد متحركاً وما
    قبله ليس منه، جاء على أثقل أحواله، فيقلب ألفاً لينتقل إلى أخف أحواله، و
    هو سكونه و ما قبله منه، فلما صار لغاة آنسهم هذا التغير بتغير آخر،
    فحذفوا الألف تخفيفاً، و لأن هذا الوزن قليل في الأسماء المفردة، و إنما
    هو من صيغ الجموع، مثل قضاة ورماة، فصار لغة كما ترى. و من عادتهم أن
    يتبعوا نقص التغير نقصاً. ألا ترى إلى قولهم ليس و أصلها ليس، بوزن فعل،
    فكان قياسهم يوجب أن تنقلب الياء ألفاً لتحركها و انفتاح ما قبلها فيقال:
    لاس، ما انقلبت ياء بيع فقيل: باع، فلما وجدوها قد خالفت في التصرف، و
    لزمت باباً واحداً، و هو صيغة الماضي لعلة أخرى لا نرى الإطالة بذكرها في
    هذا الموضع، آنسهم ذلك بتغيرها ثانياً، و جراهم على ركوب الخلاف بها عودا
    بعد بدءٍ، فقالوا فيها: ليس، و فارقوا بها أخواتها و لم يقولوا: لاس، مثل
    زال و صار، و هو فعل من الليس، و هو الشدة و الشجاعة، و كأن تقدير قولهم:
    ليس زيد قائماً، و امتنع و صعب أن يكون زيد قائماً، هذا على رأي النحويين.
    فأما أهل اللغة فيحكون عن الخليل أنه قال: ليس إنما أصله لا أيس، لأن أيس
    عنده لفظة يخبر بها عن الموجود، فإذا قالوا: لا أيس فكأنهم قالوا: لا
    موجود، فثقل عليهم فقالوا: ليس، و الأيس في كلام العرب التأثير، فأخبره به
    المتكلمون عن الموجود، لأن الموجود لا بد أن يكون له أثر أو تأثير أو نحو
    ذلك. و قال الأصمعي في كتاب " نظائر الأفعال " : آسه ييئسة أيساً إذا ثر
    فيه، و منه أيسه يويسه بوزن فعله، يفعله مشدداً، و أنشد يعقوب:

    إن كانت جلمود صخر لا أؤيسه ... أوقد عليه فأحميه فينصدع

    إن قناني لنبع لا يؤيسها ... عض الثقاف و لا دهن و لا نارُ
    و قال أبو خلدة اليشكري - بخاء مفتوحة معجمة من فوق واحدة - قال
    أبو بكر ابن دريد: و من غير ذلك فقد أخطأ، و هو ابن عبيد بن منقذ بن حجر
    بن عبد الله بن سلمة بن حبيب بن عدي بن جشم بن غنم بن حبيب بن كعب بن يشكر
    بن وائل:

    ما يسر الله من خير قنعت به ... و لا أموت على ما فاتني جزعا

    و لا يؤنس من عودي خوالقه ... إذا المغمز منها لان أو خضعا

    خوالقه: الأحداث التي تملسه، و الأخلق: الأملس.

    و يكون وجه هذا الاشتقاق إنها أعنى اللغة، لما كان أهلها ملازمين لها، و
    كانت قد صارت كالسمة لهم، و الشيء اللاصق بهم، كانوا كأنهم قد أغروا
    بملازمتها، و أوزعوا الدؤوب عليها، و كانت هي للصوقها بصفاتهم كالمغراة
    أيضاً بهم، فلهذا جاز فيها فعله بتحريك العين، لأن فعله اسم فاعل، مثل
    ضربة لفاعل الضرب و حفظة للحافظ.

    و القول الثاني: أنها من اللغو، و هو النطق، و منه سميت لواغي الناس، أي
    أصواتهم و منطقهم، و هذا من المصادر التي جاءت على فواعل، و هي قليلة، مثل
    قولهم سمعي رواغي الإبل، و ثواغي الشاة، يعني رغاءها و ثغاءها. قال حميد
    بن عبد الله بن عامر بن أبي ربيعة بن نهيك ابن هلال بن عامر بن صعصعة، و
    قد قال بعض النسابين في نسبه قولاً آخر،و هذا أحب إلي لأني رأيت أبا علي
    الهجري يرويه عن شيوخه من الأعراب و كان الهجري أعلم المتأخرين بالنسب، و
    غير الهجري أيضاً يروي عن أبي عمرو و غيره من الروات:

    رعين المرار الجون من كل باطن ... دميث جمادى كلها و المحرما

    إلى النيرو العلباء حتى تبدلت ... مكان رواغيها الصريف المسدما

    و يقول: استبدلت عوض الرغاء الذي منها يأتي ضراً و هزلاً الذي يأتي سميناً
    و أشرا.

    و قواضي الديفان: يراد به قضاؤه. قال أبو وجزة يزيد بن عبيدة من بني سعد
    أبن بكر من هوزان:

    وإذا قطمتهم قطمت علاقماً ... و قواضي الذيفان فما تقطمْ

    و النواطق: يعنون به النطق. قال كعب بن زهير المزني:

    و أدركت ما قد قال قبلي لدهره ... زهير و إن يهلك تخلَّد نواطقهْ

    و الأوامر و النواهي: يعنون الأمر و النهي، كذا قال اللغويون.

    و قد يجوز أن يكون لواغي جمع لاغية، و كذلك كله يجوز أن يكون جمعاً، و
    الأوامر و النواهي يذهب به إلى العزمات و الإرادات إلا أن الأول محفوظ
    القوم.

    و من اللغو المنطق قول الله عز و جل: ( و قال الذين كفروا لا تسمعوا لهذا
    القرآن و الغوا فيه). قال الخليل: معناه من اختلاط الكلام، و الله اعلم
    بكتابه و منه في الحديث: " من قال في الجمعة صه، فقد لغا " أي تكلم، كذا
    قال الخليل أيضاً. و مثله قول ثعلبة أبن صعير بن خزاعي بن مازن بن عمرو بن
    تميم بن مرِّ بن أدَّ.

    أسمي ما يدريك أن رب فتية ... بيضٍ الوجوه ذوي ندى و مآثر

    باكرتهم بسباء جونٍ ذارع ... قبل الصباح و قبل لغو الطائر

    أي قبل تنطق الطيور، و يقال منه: لغت الطيور و الناس أيضاً تلغو، و لغيت
    تلغى إذا نطقت و سمع نطقها، و قال عبد المسيح بن عسلة، أخو بني مرة بن
    همام بن مرة بن ذهل أبن شيبان - وقد رويت لغيره و هي له أثبت عند المفضل
    بن محمد بن يعلي الضَّبيِّ:

    و عازب قد على التهويل جنبته ... لا تنفع النعل في رقراقه الحافي

    صبحته صاحباً كالسيد معتدلاً ... كأن جُوُّجُوُّهُ مداك أصداف

    - جعل المداك من صدف لأنه أحسن له و أكثر إشراقاً.

    باكرته قبل أن تلغى عصافره ... مستخفياً صاحبي و غيره الخافي

    لا ينفع الوحش منه أن تحذره ... كأنه معلق منه بخطاف

    إذا أواضع منه مرَّ منتحياً ... مرَّ الأتي على برديِّه الطافي

    و هذا الشعر من حسان أبيات المعاني.

    و يقال أيضاً للصوت اللَّغا مقصور، يكتب بالألف مثل اللَّغو قال الجعدي:

    و عادية مثل الجراد وزعتها ... لها قيروان خلفها متكتب

    كأنَّ قطا العين التي فوق ضارج ... خلاف لغا أصواتها حين تقرب
    شبه لغط القطا باختلاف أصوات الخيل، و خلاف هاهنا بمعنى اختلاف،
    و يقال: هذه لغتهم التي يلغون بها أي ينطقون بها كما يقال هذه لعبتهم التي
    يلعبون بها، و وزنها على ذلك فعلة اصلها لغوة، فلما وجدوا الواو متحركة
    قبلها ساكن وجب ثبوتها و ذلك مستكره عندهم لنقصان فضيلتها به، و ذلك أنها
    إذا تحركت نقص المد فيها، و ثقلت زيادة على ثقلها، فاحتيل في تغيرها بأن
    حركوا العين قبلها، فلما تحركت العين، انقلبت الواو ألفاً فصارت لغات،
    فحذفوا الألف تخفيفاً على المذهب المتقدم في الجراءة على 00 بالتغيير،
    فصارت لغة، و أهل اللغة يقولون حذفت الواو للنقص، و يقتصرون من الاعتدال
    على هذا القول.

    و القول الثالث: إنها مشتقة من اللَّغو، و هو الذي لا يعبر لقلته، و لا
