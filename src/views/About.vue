    و أما الزبير بن بكار بن عبد الله بن مصعب بن ثابت بن عبد الله
    بن الزبير بن العوام بن خويلد بن أسد بن عبد العزى بن قصي فكان يذكر عن
    مالك الفقيه و هو مالك بن أنس بن مالك بم أبي عامر بن عمرو بن الحارث بن
    غيمان بن خثيل بن عمرو بن ذي أصبح - و إليه تنسب السياط الأصبحية - و اسمه
    الحارث بن مالك بن زيد بن الغوث بن سعد بن عوف بن عدي بن مالك بن زيد بن
    سهل بن عمرو بن قيس بن معاوية بن جشم بن عبد شمس بن وائل بن الغوث بن قطن
    بن عريب بن زهير بن أيمن بن الهميسع بن حمير - و اسمه العرنجج بن سبأ - و
    أسمه عبد شمس، أبن يشجب بن يعرب بن قحطان، و قد قيل في نسبه قول آخر، و
    هذا أصح و أثبت من طريق الكلبي، و لا بأس عند ذكر مالك يذكر نسب منازعيه
    الرئاسة من الشافعي و أبي حنيفة، فأما الشافعي فهو محمد بن إدريس بن
    العباس بن عثمان بن شافعي - و إليه ينسب - ابن السائب بن عبيد بن عبد يزيد
    بن هاشم بن المطلب بن عبد مناف.

    و أما أبو حنيفة فهو النعمان بن ثابت مولى بني تيم الله بن ثعلبة بن عكابة
    بن صعب بن علي بن بكر بن وائل، ذكر ذلك ابن أبي خيثمة و هو الذي يروى عنه
    أبو عبد الله محمد بن داود ابن الجراح - رحمه الله - وقد قيل انه ليس له
    عرق في العرب. و لا ولاء أيضاً، و أنه يدعي ولاء بني تيم الله، و ذكر أبو
    عبد الرحمن المعري قال: قال لي أبو حنيفة ممن أنت؟ قلت: من أهل دورق. قال:
    ما يمنعك أن تعتزي إلى بعض أحياء العرب فهكذا كنت، حتى إعتزيت إلى هذا
    الحي من بكر بن وائل، فوجدتهم حي صدق، و الله أعلم بالصحيح.

    و ما أعرف في الثلاثة من يروى له شعر إلا الشافعي، فإن أبا بكر محمد بن
    شعيب الصيرفي القاضي الحلبي - رحمه الله - أنشدني قال: أنشدني بن أبي
    العقب الدمشقي بإسناد وصله إلى الشافعي، للشافعي:

    يا راكباً قف بالمحصب من منى ... واهتف بقاع خيفها و الناهضِ

    سحراً إذا قاض الحجيج إلى منى ... دفعاً كملتطم الفرات الفائضِ

    إن كان رفضاً حبُّ آل محمد ... فليشهد الثقلان أني رافضي

    فقال الزبير عن مالك: إن جزيرة العرب المدينة، و اسمها طيبة، و قد تسمى
    بأسماء غير ذلك منها طيبة و طابة، و يثرب و العذراء و جابرة و يندد و
    الدار، قال الله عز و جل: (و الَّذينَ تَبَوَّأوا الدَّارَ و الإيمان) و
    يحتج قائل هذا بقول عمر بن الخطاب بن نفيل بن عبد العزى ابن رياح بن عبد
    الله بن قرط بن رزاح بن عدي بن كعب بن لؤي بن غالب، و قد قال له كعب: إنك
    تموت شهيداً. فقال: من أين و أنا في جزيرة العرب؟! و إنما نُحِلَت هذا
    الاسم من أجل أنه كان بنو عبيل بن عوص أخوة عاد بن عوص بن ارم بن سام بن
    نوح عليه السلام، ثم نزلها بعدهم بنو عمليق بن يلمع بن عابر بن أشليحا بن
    لوذ بن سام بن نوح عليه السلام، ثم نزلها بعدهم قوم من عاد بن عوص، و كل
    هؤلاء من القبائل القديمة التي تدعى العرب العاربة، و هي عاد و عبيل أبناء
    عوص، و ثمود و جديس ابنا غاثار بن ارم، و طسم و عمليق و جاسم و أميم بنو
    يلمع بن عابر.

    و روى أبو أسامة: أَميم - بفتح الهمزة - و غيره قال: أُميم - بضمها - و
    حضرموت و السلف و الموذ، بنو يقطان بن عابر، و جدهم بن عامر بن سابن يقطان
    بن عابر بن حمير، فهم فيهم، قالوا: فأقام أولئك القوم من عاد بيثرب برهة،
    حتى جاءهم قوم من الأزد - و اسم الأزد دراء بوزن فعال، و يقال فيه: الأسد
    بالسين، و ذكر يعقوب أنه أفصح في الزاي و النسابون يقولون: إن الأسد إنما
    سمي الأسد لكثرة إسدائه المعروف، و هذا اشتقاق لا يصح عند أهل النظر، و
    الصحيح في اشتقاق ما أخبرني به أسامة عن رجاله قال: العسد و الأسد و الأزد
    هذه الثلاث كلمات معناها كلها الفتل قال: و الأزد يكون أيضاً بمعنى العزد،
    و هو النكاح. فنفى الأزديون العاديين عن يثرب و تدبرها، و أقاموا بها، و
    في ذلك يقول شاعر من الخزرج ثم أحد بني زريق في قصيدة طويلة يصف قومه:

    ملوكاً على الناس لم يملكوا ... من الدهر يوماً كحل القسم

    فأنبوا بعادٍ و أشياعها ... ثمود و بعض بقايا ارم

    بيثرب قد شيدوا في النخيل ... خصوناً و دجن فيها النعم

    و فيما أشتهوا من عصير القطاف ... و العيش رخو، على غيرهم
    فساروا إليهم بأثقالهم على كل فحل هجانٍ قطم

    فما راعهم غير معج الخيول ... و الزحف من خلفهم قد دهم

    فابنا بسادتهم و النساء ... رغماً، و أموالهم تُقتسم

    ورثانا مساكنهم بعدهم ... فكنا ملوكاً بها لم نرم

    و قد استقصيت ذكر هذه القبائل و أصل نقلها و مناهي أمورها في موضعه من هذا
    الكتاب، و بالله التوفيق.

    و إنما نعني بجزيرة العرب محلها الأقدم، و مركز بيضهم الأول، لأنهم بع ذلك
    تفسحوا و حازوا كثيراً من الأرض خارجاً من هذه الجزيرة، ألا ترى إلى قول
    الأخنس بن شهاب - و سيأتي نسبه و خبره مستقصى في كتاب تغلب إن شاء الله:

    لكل أناس من معد غمارة ... عروض إليها يلجأون و جانبُ

    لكيز لها البحران و السيف كله ... و إن يأتها بأس من الهند كاربُ

    تطاير على أعجاز حوش كأنها ... جهام أراق ماءه فهو آيبُ

    - أبو عمرو الشيباني: يطيروا - لكيز: بن أقصى بن عبد القيس بن دعمي بن
    جديلة بن أسد بن ربيعة ابن نزار.

    و بكر لها طهر العراق و إن تشا ... يحل دونها من اليمامة جانبُ

    يعني بكر بن وائل بن قاسط بن هنب بن أقصى بن جديلة بن أسد بن ربيعة ابن
    نزار، و من قبائل بكر: يشكر، و زمان و شيبان و عجل و لخم و بنو قيس بن
    ثعلبة و غيرهم.

    و صارت تميم بين قف و رملة ... لها من حبال منتاى و مذهبُ

    و كلب لها خبث فرملة عالجٍ ... إلى الحرة الرجلاء حيث تحابُ

    في الكلام على نسب كلب طويل، و نحن نذكره في موضعه إن شاء الله.

    و غسان حي عزهم في سواهم ... يجالد عنهم مقنب و كتائب

    - يعني أنهم عمال الروم - قد شرحنا نسب غسان في غير هذا الموضع من كتاب
    كندة، و بالله التوفيق.

    و بهراء حي قد علمنا مكانهم ... لهم شرك حول الرصافة لاحبُ

    بهراء بن عمرو بن الحاف بن قضاعة، زهط المقداد بن عمرو.

    و غارت إياد في السواد و دونها ... برازيق عجم تبتغي من تضارب

    يعني إياد بن نزار بن معد، و كان يقال لهم الطبق لشدة إطباقهم بالشر و
    العُرام على الناس، و منهم قيل: وافق شنٌّ طبقةً، يعنون شنَّ بن أقصى بن
    عبد قيس كذا قال الكلبي:

    و لخم ملوك الناس تجبى إليهم ... إذا قال منهم قائل فهو واجبُ

    لخم بن عدي بن الحارث بن أددٍ، و كان اسم لخم مالك، و من لخم بنو نصر قوم
    النعمان بن المنذر ملك الحيرة، و سنستقصي هذا في موضعه إن شاء الله.

    و نحن أناس لا حجاز بأرضنا ... مع الغيث ما نلقى و من هو غالبُ

    يعني أنا مصحرون لا نخاف أحداً، نكون مع الغيث حيث أغن و مع الغلب حيث عن،
    و مثل نحو من هذا القول قول ذباب بن معاوية العكلي:

    و نحن أناس لا حجاز بأرضنا ... نلوذ به إلا السيوف البواترُ

    و قال سويد بن كراع العكلي و قال اللغويون: إن كراع اسم أمه، و إن اسم
    أبيه عمرو أو عمير، الشك مني:

    و نحن أناس لا حجاز بأرضنا ... نلوذ به إلا السيوف القواطعُ

    و لم يبق منا القتل إلا عصابة ... تطاعن عن أحسابنا و تقارعُ

    و أبيض لا يشترى بشيءٍ أفاته ... أسنّتنا و النقع أغبر ساطعُ

    تركنا عليه قصدتي سمهريةٍ ... و جريان سيفٍ سلَّه إذ تُماصعُ

    و في هذا البيت الرابع غرض حسن، و معنى بديع، و يشبه قول ناجية بن الأسود
    الجرمي، و كان بعض السلاطين جلده، فأخذ سيفه من عنقه و ضربه به:

    و لما علاني بالقطيع علوته ... و بالكف صافٍ كالعقيقة قاطعُ

    فطار بكفي نصله و رياسه ... و في جيد سعدٍ غمدهُ و الرصائعُ

    و في نحو من هذا يقول ابن زيانة التميمي - زيابة بوزن فعالة مشددة كذا
    فرآنا على جماعة من الأشياخ، و روى محمد بن داود بن الجراح عن رجاله: ابن
    زيانة بوزن فعالة تخفيفا ً - و الزيابة الفأرة - و في المثل: أسرق من
    زيابة، يعنون به الفأرة، و لا أحسب أبا عبد الله محمد بن داود إلا و قد
    أوهم في هذه اللفظة، لأن الرجل يقول في شعره:

    أنا ابن زيابة إن تدعني ... آتيك و الظن على الكاذب
    قال محمد بن داود: و اسمه عمرو بن الحارث بن همام أحد بني تيم
    الله ابن ثعلبة، و البيت الذي أردنا إنشاده لمشابهته ما تقدم قوله:

    و الله لو لاقيته خالداً ... لآب سيفانا مع الغالب

    و الثالث: - من و جوه اشتقاق العرب - أنه مأخذه من العرب و هي النفوس، و
    أحدثها عربهٌ، مثل قصبةٍ و قصبٍ و أنشدنني أبو أسامة لابن ميادة الرماح بن
    أبرد المري:

    لما أتيتك أرجو فضل نائلكم ... نفحتني نفحة طابت لها العربُ

    و قال: العرب هاهنا النفوس.

    و يكون وجه هذا الاشتقاق على معنيين: أحدهما: أنهم من الخلق بمنزلة النفس
    من الجسم، و ثانيهما: أنهم أعز الخليقة نفوساً، و المعروف ما انفردوا به
    من صلابة الأكباد و قوه الأرواح و عزة النفوس أشهر من أن نطلب عليه
    شاهداً، و منه قول شاعرهم:

    لا شيء أحسن منها إذ تودعنا ... و جيبها برشاش الدمع مغتسل

    يبكي علينا و لا نبكي على أحد ... لنحن أغلظ أكباداً من الابل

    و قائل هذا الشعر قدير بن منيع، أو أبوه منيع بن معاوية بن فروة ابن
    الاحمس بن عبدة بن خليفة بن جرول بن منقرٍ، خلاف هذا الشعر أنشدني لهدبة
    بن خشرم بن كرز بن أبي حية بن الاحسم بن عامر بن ثعلبة بن قرة بن خنبس بن
    عمرو بن ثعلبة بن عبد الله بن ذبيان بن الحارث بن سعد بن زيد بن ليث بن
    سود بن أسلم بن الحاف بن قضاعة:

    باتوا وأتبعهم عيناً مملاة ... دمعاً و ما ذاك إلا الشوق و الطرب

    نبكي عليهم و لا يبكون فرقتنا ... أنا و جيراننا من جيرة عجب

    و ليس بالمطرح في إباء النفس قول إبراهيم بن إسماعيل بن يسار بن فيروز أبن
    باذام، مولى بني تيم بن مرة:

    و نفسي النفس تأبى أن أواتيها ... على الصغار و تأبى أن تواتيني

    من نفس إبليس شقت في حميتها ... لا نفي آدم في عطفٍ و في لين

    و كان أبوه إسماعيل شاعراً في زمن عبد الملك و والده. و لمصنف الكاتب شعر
    في الإبا قد أطاع هواه فورده و هو:

    قارعت الأيام مني امرأ ... قد أعلق المجد بامراسه

    تستنزل النجدة من رأيه ... و يستدر العز من باسه

    أروع لا ينحط عن تيهه ... و السيف مسلول على رأسه

    و لعلي بن محمد بن يوسف جد أبي شعر يقول فيه:

    و إني على الإقتار همة ... لها مسلك بين المجرة و النسر

    أؤمل نفسي لا أؤمل غيرها ... من الناس أو يأتي الغنا و هو ذو صغر

    و قد أنشدوني لشاعر من قريش، قريش الأندلس شعراً يقول فيه:

    و لو صدت نجوم الليل عني ... كصدك ما نظرت إلى السماء

    و كيف تظن نخوة عبشمي ... تكون فيه قلب مزيقياءٍ

    و لما قدمناه من التفسير سموا يوم الجمعة العروبة لاستكمال خلق النفوس و
    غيرها من المخلوقات فيه على ما يذكره رواة مبتدأ العالم. و من ذلك أيضاً
    قيل له: الجمعة، لأن فيه اجتماع المخلوقات. و مما يدل على شرف هذا اليوم و
    استحقاقه لهذا الاسم أن الأيام تسمي بأسماء مشتقة لها من العدد في أكثر
    اللغات غيره، فأنه خص باسم عليه و قال رسول الله ( لما قيل له: إن السبت
    لليهود و الأحد للنصارى: " نحن الآخرون السابقون " يعني الآخرون زماناً و
    السابقون يوماً، و يروى أن رياح بن الربيع التميمي أخا حنظلة الذي يدعى
    بحنظلة الكتاب، و هو حنظلة ابن الربيع بن صيفي بن رياح بن الحارث بن مخاشن
    بن جهور بن غوى بن جروة بن أسيد بن عمرو بن تميم - قال لرسول الله ( :
    للنصارى يوم و لليهود يوم، فلو كان لنا يا رسول الله يوم!! فنزلت سورة
    الجمعة.و يقال: عروبة بلا ألف و لام، و أنشد أهل اللغة: يوماً كيوم عروبة
    المتطاول.

    و يرون أن تعريفه بنفسه أفصح من تعريفه بالألف و اللام على انه قد جاء في
    الشعر الفصيح، و قال الأعشي الكبير:

    فبات عذوباً للسماء كأنما ... يوائم رهطاً للعروبة صيَّما

    يوائم: يشابه. و في المثل: لولا الوئام هلك الأنام - يعني لولا تشبههم
    بالكرام. وقد يقال فيها: العروب - بلا هاء - قال عباية بن شكس العنزي ثم
    الهزاني:

    أنا العنزي بن الأسود الذي بهم ... أسامي إذا ساميت أو أتبحجُ
    هم أسروا يوم العروب ابن طالم ... و أردوا مريئاً فهو للشق مجنحُ

    و يروى: الغروب، و الأول أثبت.

    و إذ قد فسرنا عروبة فالوجه اتباعه بتفسير بقية الأسماء العربية للحاجة
    إلى ذلك. فأولها: الأحد، و اسمه عندهم أول، لأنه أول الأيام، و لذلك أيضاً
    سمي الأحد، لأن منه ابتداء العدد، و أصله واحد، و أبدل من الواو ألفاً
    استثقالاً للابتداء بالواو، و قال نابغة ذبيان:

    كأن رحلي و قد زال النهار بنا ... بذي الجليل على مستأنسٍ وحدِ

    و يجمع على آحاد و أوحاد، و مثله أناة و وناة للجارية الجميلة الخريدة
    فأما الأناة الاسم من التأني فلا يكون إلا بالالف لأن أصلها من تأنيت،
    تفعلت بمعنى تنظرت الشيء إذا آن، أي حان و دنا وقته، و منه قول يحيى بن
    يعمر الوشقي - من وشقة بوزن فعلة - بن عوف بن بكر بن يشكر بن عدوان بن
    عمرو بن قيس بن عيلان: أي مالٍ أديتَ زكاته فقد ذهب أبلته، قال أبو عبيدة:
    أصله وبلته - أي شره و مضرته - مأخوذ من الوبيل، و هو الضار.

    و يسمى يوم الاثنين أهون، لأنه مكروه عندهم، و لكنه أهون طيرة، و أيسر
    كراهية من يوم الأربعاء، ويسمى أوهد أيضاً مشتقاً من الوهدة و هي
    الانحطاط، لانخفاض العدد من الأول إلى الثاني.

    و يسمى الثلاثاء جباراً لأنه هدر لا يكره و لا يستحب، و كذلك كل ما لم
    يعتد به قيل له جباراً، و من هاهنا قيل ذهب دمه جباراً أي هدراً. قال
    الأفوه - واسمه صلاءة بن عمرو أبن عوف بن منبه بن أود بم صعب بن سعد
    العشيرة بن مالك بن أددٍ، قال ابن الكلبي: أود ابن معد بن عدنان و إنما
    انتقلوا فقالوا: أود بن صعب - :

    حكم الدهر علينا أنه ... ظلف ما نال منا و جبار

    جبار: يعني هدراً و منه: جرح العجماء جبار، و أصله من جبرت العظم و كأن
    هذه الأشياء السهلة الهنية يلغى ذكرها جبراً للمسلم، و اعتماداً لشمل
