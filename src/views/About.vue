    لام الفعل و هي تعتل كاعتلال حروف المد اللين، مع ما فيها من الضعف بكونها
    أخيراً، و من الاستعداد للتغيير بالتخفيف و غيره، فقل احتمالها للإعراب، و
    كان ضعفها و عجزها من الإعراب الذي هو حلي الكلمة و قيم معناها كعمدها، و
    حضورها عندهم كمغيبها، فعدوا هذا الاسم مما آخره محذوف، و رأوا أنه يستحق
    شيئاً من آلات الفعال لمشابهته إياها بهذا النقص، فأعطوه ألف الوصل في
    أوله، بعد أن اسكنوا فاءه التي هي الميم، ليزيد عذرهم بياناً في إدخال ألف
    الوصل، و ليكون مورد ألف الوصل على الميم ساكنة كموردها على فاء الفعل
    الساكنة، و لتستوفي في هذا التعويض الذي أعطته كلية المشاكلة. و حركوا
    الراء لعلتين: إحداهما أنهم لما أسكنوا الميم - كما قلنا - لم يجز بقاء
    الراء ساكنه لئلا يلتقي ساكنان، و ليس يسوغ ذلك في النطق. و
    الثانية:الاحتزاز من تطرق التغيير على الهمزة بكونها متحركة قبلها ساكن. و
    اختلف بعد ذلك في حركة الراء. فبعض العرب يلزمها الفتح، فيقول: رأيت
    امرأً، و هذا امرأً، و مررت بامرئٍ، و يقول إنه لما اضطر إلى تحريكها ذهب
    بها إلى الفتح لأن أخف الحركات. و بعضهم - و هو الأكثر الأشهر - يجعل
    الراء تابعة للإعراب الواقع على الهمزة، فيقول: هذا امرؤ، و رأيت امرأً، و
    مررت بامرئٍ، و ذلك لأن الهمزة على ما قدمنا من الضعف، فعضدوها بمشاركة
    الراء لها، و تحملها الإعراب معها، و من أجل هذا لم يضموا الألف في قولهم:
    هذا امرؤ، و لم يتبعوها ضمة الراء، كما ضموضها في قولك: " أقتل " اتباعاً
    لضمة التاء، و ذلك لأن الراء ليست الضمة لازمة لها، و إنما هي أحد وجوه
    تصاريفها. و بعض الناس يرى أن الإعراب واقع على الراء، و إن حركة الهمزة
    مفحمة لا يعتد بها مثل قولهم: يا طلحة أقبل، و هم يريدون: يا طلح أقبل. و
    لو أرادوا المناداة باسمه من غير ترخيم لقالوا: يا طلحة أقبل، بضم الهاء
    على حكم الاسم المفرد، و هذا القول غير طائل في قولهم: امروء، من أجل أنه
    يحصل الأعراب في وسط الاسم، و ذلك لا يتمكن في كلامهم، و العلة في ذلك أنه
    لا يكون في أوله من أجل استحقاق الأول للحركة، إذ كان لا يقع الابتداء
    بساكنٍ، فلما استحق الحركة اللازمة كان بمنزلة ما استحق البناء فلم يعربوه
    في أوله، لأن الإعراب يتغير و ينتقل، و من رأيهم أن يبنوه، و لم يجز أن
    يعربوه في وسطه لئلا يختل نظام الأبنية، فلم يبق غير آخر فأعربوه فيه، و
    وجه آخر: أن الإعراب يستحقه الاسم بعد تمام الاسمية فيه، فكان الوجه على
    هذا أن يعرب في آخره على نحو قولهم: امرؤ، و اتباعهم الراء حركه الهمزة،
    أجروا قولهم ابنم، و قد استقصينا شرحه بعلله و شواهده في غير هذا الكتاب.

    و أما قولنا: امرأة فحركت الراء بالفتح، لأنه أخف الحركات، و تحصنت عن أن
    تتبع حركة الإعراب، لأن الهمزة استغنت عن حرف يعينها في حمل الإعراب،
    لانتقاله عنها إلى الهاء الموردة للتأنيث مع استفادتها - أعني الهمزة -
    بعض القوة لتوسطها و احتجازها عن التطرف بالهاء التي بعدها. و الكلام في
    هذه المسألة يتسع لأكثر مما قلناه، و لكنا نرى الاختصار.

    و إذا نسبوا إلى امرئ القيس قالوا: امرئي بوزن امرعي بكسر الراء، و
    اقتصروا بالراء على الكسر لمكان ياءي النسبة و استدعائهما الكسر في
    المشاكلة، هذا القياس عند أبي بشر عمرو بن عثمان بن قنبر، مولى بني الحارث
    بن كعب، من مذحج، و عند عامة النحويين، و الذي تكلمت به العرب مرئي، بوزن
    مرعي، و هو عند أهل العربية على الشذوذ، و قال ذو الرمة غيلان بن عقبة بن
    بهيشٍ - بالشين معجمة - بن مسعود بن حارثة بن عمرو بن ربيعة بن ساعدة بن
    كعب بن عوف بن ثعلبة بن ربيعة بن ملكان - في قضاعة ملكان و في السكون
    ملكان و الباقي كله ملكان - بن عدي بن عبد مناة بن ادبن طابخة - و اسمه
    عامر - بن الياس بن مضر، في هجائه هشام بن قيس المرئي:

    و يسقط بينها المرئي لغواً كما ... ألغيت في الدية الحوارا

    إذا رئية ولدت غلاما ... فألام ناشيء نشيغ المحارا
    و كرر ذكر المرئي في أبيات كثيرة لا نرى الإطالة بإنشادها، و
    يجوز أن تنشد بالتخفيف فتكون ألزم لطريق القياس، على أنه منسوب إلى مرءٍ
    من قولك مرء القيس.

    قال أبو بكر الأنباري: يجوز في اللفظ به أربع صيغ: تقول هذا أمرؤ القيس -
    بضم الراء - و هذا امرؤ القيس - بفتح الراء - و هذا مرء القيس - بفتح
    الميم و تسكين الراء - و هذا مرء القيس - بتسكين الراء و صم الميم - فيقال
    على هذا: المرئي، و ليس يخل بالوزن إلا أن السماع في هذا البيت و في غيره
    من كلامهم مرئي - بالتحريك - و لعلهم حركوه خيفة التباسه بقولهم: رجل
    مرئي، أي منظور إليه، و مرئي أيضاً من رايته إذا أصبت رئته، مثل: قلبته
    إذا أصيب قلبه، و مثل قولهم: قتله، و إنما يعنون أصبت قاتله أي نفسه، و
    القتال النفس قال الشاعر: يدعن الجلس نحلا قتالها و في نحو منه قولهم: بحت
    بالشيء، إنما يعنون به أظهرت ما في بوحي، و البوح النفس و قال أبو جعفر
    محمد بن حبيب مولى بني هاشم: كل امرئ القيس في العرب فالنسبة إليه مرئي
    كما جاء في شعر غيلان إلا صاحب اللواء فإن النسبة إليه مرقسي، مثل عبشمي و
    عبدري.

    و جمله القول اللغوي في هذه الكلمة أن المرء الرجل و المرأة تأنيثه، و أصل
    هذا الباب الجري، فيقال: امرأة كما يقال: جارية، لأنها تجري في نمو و
    شباب، و كذلك الرجل أيضاً، ومنه الشيء المريء، يراد به الذي يجري في
    مجاريه و يسوغ أن يقصد مصدره المراءة، بوزن المراغة، و يقال: مرأ الطعام
    يمرأ مراءة، كما يقال: قمأت الماشية تقمأ قماءة، و مرو الرجل يمرو، كما
    يقال: قمو الرجل يقمو، عن محمد بن زياد الأعرابي. و يقال: استمرأت الطعام
    و هذا يمريء الطعام أي يسهل جريه و ينفذه، و منه المريء من الحلق، و هو
    المسترط، الذي يجري فيه الطعام و الشراب إلى المعدة، و جمعه أمرئة على
    أفعلة، و مرؤ - بوزن فعل مضموم الفاء و العين - و أبو أسامة يروي هذا
    الحرف في " غريب المصف " مشدداً غير مهموز قال: هو المري، و كأنه من مريت
    الضرع أي مسحته و لينته، و سهلت مجاري اللبن فيه. قال أبو أسامة: و قد روي
    المريء مهموزاً و هو جائز حسن، فأما أبو الحسن ثابت فرواه بالهمز لا غير،
    و قال أبو عبد الله بن الأعرابي في كتابه الذي سماه بكتاب " الإبل " :
    المري - غير مهموز - التي تحلب على غير ولد، و إنما سميت مرياً لأنها تمري
    بالأيدي فلا تكون مرياً و معها ولدها.

    و يقال هنيئاً مريئاً: أي جارياً لذيذاً سائغاً قال الله عز و جل (فكلوه
    هنيئاً مريئاً) و قال كُثير بن عبد الرحمن - بن الأسود بن عامر بن عويمر
    بن مخلد بن سعيدة بن سبيع بن جعثمة بن سعد بن مليح بن عمرو بن ربيعة بن
    حارثة بن عمرو مزيقباء بن عامر ماء السماء بن حارثة بن امرئ القيس بن
    ثعلبة بن مازن الأزد - و كان يقال له: أبن أبي جمعة ينسب إلى جدة من قبل
    أمه و هو أبو جمعة الأشيم بن خالد بن عبيد بن مشبر بن رياح بن سالم بن
    غاضرة أبن حبشية، و قد رأيت بخطوط رؤساء من أشياخ النسب حبشية - مشدد
    الياء و الأول أثبت - و هو قول ابن حبيب. و الحبشية النملة الكبيرة، و
    حبشية هو ابن كعب بن عمرو و كعب أخو مليح بن عمرو - :

    هنيئاً مريئاً غير داءٍ مخامر ... لعزة من أعراضنا ما استحلت

    و قد قيل في نسب كثير قول آخر ما يضرنا ذكره: كان بعض قوم كثير يقولون:
    أنهم من ولد الصلت بن النضر بن كنانة، و في ذلك يقول كثير:

    أليس أبي بالنضر أم ليس أسرني ... بكل هجان من بني النصر أزهارا؟

    إذا ما قطعنا من قريش عصابة ... فأي قسي تحمل النبل ميسرا؟

    لبسنا ثياب العصب فأختلط السدى ... بنا و بهم و الحضرمي المخضرا

    فإن لم تكونوا من بني النضر فاتركوا ... أراكاً بأذناب الفوائج أخضرا

    قال هذا الشعر يخاطب به أبا علقمة ميسر بن حدير بن علقمة بن أبي الجون - و
    اسم أبي الجون عبد العزى - بن سعد بن ربيعة بن أصرم بن ضبيس بن حرام بن
    حبشية بن كعب فقال ميسرة يرد عليه في رواية النسابة الهاشمي من أهل حلب، و
    قال غيره: إنها للحوص - :

    لعمري لقد جاء العراق كثير ... بأحدوثة من وحيه المتكذب

    اتزعم أني من كنانة والدي ... و ما لي من أم هناك و لا أب
    و لما ذكر العراق لأن عبد الملك كان دعا كثيراً إلى هذا النسب
    فأجابه، و أجابته خزاعة الحجاز فسار كثير إلى خزاعة العراق يدعونهم فأبوا،
    و بلغ ذلك الأحوص - أو ميسرة - فقال هذا الشعر، و روى زبير بن بكار عن
    رجاله أن كثيراً ينسب نفسه و قومه إلى النضر بن كنانة، و قال لأبي علقمة
    ميسرة:

    أبا علقم أكرم كنانة إنهم ... مواليك إن أمر نبابك مقلق

    بنو النضر ترمي من ورائك بالحصا ... أولو حسب فيهم حفاظ و مصدق

    إذا ركبوا ثارت عليك عجاجة ... و بالأرض من وقع الأسنة أولق

    هم ملوكهم من تهامة منزلاً ... و لولاهم كنتم أباء يحرق

    فلا تكفرن قوماً عززت بعزهم ... أبا علقم فالكفر بالريق يشرق

    في أبيات. قل الزبير: فأجابه الأحوص بن عبد الله بن محمد بن عاصم بن ثابت
    بن أبي الأقلح - اسم أبي الأقلح قيس - بن عصمة بن مالك بن أمة بن ضبيعة بن
    زيد ب مالك بن عوف بن عمرو بن عوف بن مالك بن الأوس.

    فإنك لو أعذرت أو قلت شبهة ... لذي الحق فيها و المخاصم مغلق

    عذرناك أو قلنا: صدقت و إنما ... يصدق بالأقوال من كان يصدق

    ستأبى بنو عمرٍو عليك و ينتهي ... بهم حسب في جذم غسان و معرق

    يعني بني عمرو مزيقاء

    و إنك لا عمراً أباك حفظته ... و لا النضر إذ ضيعت شيخك تلحق

    فأصبحت كالمهريق فضلة مائه ... لباقي سراب بالملا يترقرق

    و جميع أهل العلم بالنسب يقولون: إن الصلت بن النضر درج، و يباطلون دعوى
    كثيرٍ.

    و قد قال آخرون:إن خزاعة من ولد قمعة و هو عمير بن الياس بن مضر، و أحسب
    أن القتبي روى ذلك، و الأول هو الصحيح. و كثير يكنى أبا صخر، و كان ينسب
    إلى عزة بنت جميل أبن حفص بن اياس بن عبد العزى بن حاجب بن غفار بن مليل
    بن ضمرة بن بكر بن عبد مناة بن كنانة بن خزيمة.

    و نعود إلى سبيلنا الأولى. و قال عبد الله بن همام أحد بني مرة بن صعصعة
    أبن معاوية بن بكر بن هوزان بن منصور بن عكرمة بن خفصة بن قيس بن الناس
    بالنون و هو عيلان بن مضر. و أم ولد مرة بن صعصعة سلول بنت ذهل بن شيبان
    بن ثعلبة بن عكاية أبن صعب بن علي بن بكر بن وايل فإليها ينسبون:

    اشرب شرابك و انعم غير محسود ... و اكسره بالماء لا تعض أبن مسعود

    إن الأمير له في الخمر مأربة ... فأشرب هنيئاً مريئاً غير تصريد

    و يعني عامر بن مسعود بن أمية بن خلف بن وهب بن حذافة بن جمح بن عمرو بن
    هصيص بن كعب بن لوي بن غالب بن فهر بن مالك بن النضر بن كنانة. و كان اسم
    جمح بن عمرو تيماً، و اسم أخيه سهم بن عمرو زيداً، فقعدت يوماً أمهما
    الألوف بنت عدي بن كعب بن لؤي بن غالب، و معهما أترجة من ذهب، و قيل من
    فضة، فدحت بها و قال لأبيها: استبقا إليها فمن أخذها فهي له. فأخذها سهم.
    فقالت: كأنك و الله يا زيد سهم مرق من رميته، و كأن شيئاً جمح بك عنها يا
    تيم، فسمي هذا جمح و سمي هذا سهماً.

    و عامر بن مسعود هذا كان يلقب دحروجة الجعل لقصره، و في ذلك يقول عبد الله
    بن همام السلولي:

    واشف الأرامل من دحروجة الجعلِ

    و اصطلح أهل الكوفة على عامر بن مسعود عند موت يزيد بن معاوية، و هرب عبد
    الله بن زياد، فأمروه عليهم، فأقره عبد الله بن زيد اشهراً ثم عزل، و خطب
    أهل الكوفة في أيام إمارته عليهم فقال: إن لكل قوم أشربة و لذات فاطلبوها
    في مظانها، و عليكم بما يجمل منها و يحل، و اكسروا شرابها بالماء، و
    تواروا عني بهذه الجدارات فلذلك قال عبد الله بن همام الشعر المتدم، و قال
    فيه غيره من الشعر:

    من ذا يحزم ماء المزن خالطه ... في قعر خابية ماء العناقيد

    إني لأكره تشديد الرواة لنا فيها و يعجبني قول ابن مسعود و لما بلغ ابن
    مسعود قول عبد الله بن همام قال: قطع الله لسانه عدل الحمار فقد أساء
    القول. ذهب إلى قول الأخطل:

    بئس الفوارس عند مختلف القنا ... عدلا الحمار محاربٌ و سلولُ

    و قال آخر - أنشدنيه محمد بن الحسين عن إسحاق بن إسماعيل الطاهري عن
    المبرد - :

    كل هنيئاً و ما شربت مريئاً ... ثم قم صاغراً فغير كريم
    لا اُحب النديم يومض بالعين ... إذا ما انتشى لعرس النيم

    و المروؤة مصدر المرء كالرجولة مصدر رجل، و قال الفراء: أنه سمع إعرابياً
    قيل له: فلان امرؤ سوءٍ فقال: و ما رأيت من مروؤة سوئه؟ و لا فرق عندي بين
    أن يقال: في فلان مروؤة و بين أن يقال: فيه رجولة، أو فيه إنسانية، إلا أن
    المروؤة قد نقات عن أصلها الذي وضعت له إلى صفات خاصة.

    و قد أحكمنا أمره وضع الأسماء و نقلها في كتاب غير هذا.

    و اختلف الناس بعد في المروؤة، و أحسن الأقوال فيها ما أخبرنا به عن أمير
    المؤمنين على - صلوات الله عليه - إنه قال: المروؤة موصوفة في كتاب الله
    عز و جل حيث يقول: (إنَّ اللهَ يأمرُ بالعدلِ و الإحسانِ، و إِيتاء ذي
    القربى، و ينهى عن الفحشاءِ و المنكرِ و البغي) و قال السمي القاضي:
    المروؤة أن لا تصنع شيئاً في السر تستحيي منه في العلانية. و قال الحسن بن
    سهل - و أحسن - : نعم العون على المروؤة احتشام العامة. و الكلام في هذا
    طويل لا يقتضيه هذا الموضع.

    و يقال للرجل: مريء إذا كان ذا مروؤة، على وزن فعيل، كما يقال له: رجيل،
    إذا كان ذا رجولة، قال عبد الله بن غويث الفقيمي - من فقيم بن جرير بن
    دارم - :

