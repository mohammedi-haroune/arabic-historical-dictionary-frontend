    و لما مات محمد بن أبي العباس و قد " ؟ " الكوفة و البصرة محمد بن سلمان
    بن علي أخو زينب، طاب حماداً طلباً حثيثاً فاستجار حماد بقبر سلمان بن علي
    و قال:

    إن أكن مذنباً فأنت ابن من كا ... ن لمن كان مذنباً غفاراً

    يا ابن بنت النبي إني لا اجعل ... إلا إليك منك الفرارا

    غير إني جعلت قبر أبي أيو ... ب لي من حوادث الدهر جارا

    لم أجد لي من الأنام مجيراً ... فاستجرت التراب و الأحجارا
    فلم ينفعه ذلك شيئاً عند محمد بن سلمان، فكتب إليه:

    قل لوجه الخصي ذي العار إني ... سوف أهدي لزينب الأشعارا

    قد لعمر فررت من شدة الخوف، و أنكرت صاحبي جهارا

    و ضننت القبور تمنع جاراً ... فاستجرت التراب و الأحجارا

    فإذا القبر ليس لي بمجيرٍ ... فحشى الله ذاك القبر نارا

    و ما أحسبه ظفر به. و نعود فنجدد من شأننا - بتوفيق الله - .

    قال صاحب اللواء:

    إذا المرء لم يخزن عليه لسانه ... فليس على شيء سواه بخزان

    و قال أيضاً:

    و جرح اللسان كجرح اليد

    و تبعه الأخطل فقال:

    أفحمت عنكم بني النجار و قد علمت ... عليا معد، و كانوا طال ما هدروا

    حين إستكانوا و هم مني على مضضٍ ... و القول ينفذ ما لا تنفذ الأبر

    و قال غيره - و أحسبه طرفة - :

    فإن القوافي يتلجن موالجاً ... تضايق عنها أن تولجها الأبره

    قد ألم بهذا اللفظ دون المعنى أبو نصر بن نباتة، بقية شعراء العراق، و هو
    مادح أبي وجدي:

    فلا تحتقرن عدواً رماك ... و إن كان في ساعديه قصر

    فإن الحسام يقد الرقاب ... و يعجز عما تنال الابر

    و قال طرفة - و احسن - :

    و تقصد عنك مخيلة الرجل ال ... عريض، موضحة عن العظم

    بحسام سيفك أو لسانك و ال ... كلم الرصين كأغرب الكلم

    و قال عمر بن عبد العزيز: التقي ملجم. و حدثنا أن يونس - عليه السلام -
    قيل له من بعد خروجه من بطن الحوت، و قد أطال الصمت: لم لا تتكلم؟ فقال:
    الكلام صيرني إلى بطن الحوت. و روى أن رسول الله صلى الله عليه و آله أنه
    قال: " إن من الكلام عيالا " و أنا أرى أن هذه اللفظة من الكلم التي أعطي
    رسول الله صلى الله عليه و آله جوامعها لحسنها و بيانها و إيجازها. و قال
    بعض عامة بلدانا الحلبيين في قصة له: رب كلمه، أزلت نعمة، و قال الشاعر:

    إن كان في العي آفات مقدرة ... ففي البلاغة آفات تساويها

    و هذا كثير لا ينساه إلا الساهي، و باهر لا يذل عنه إلا المحين، و قد
    أنشدني منشد من أبيات بيتاً صحيح القسمة، يعجبني، و هو:

    هي الخطوب فمن ماضٍ و منتظر ... و هو الأنام فمن ساهٍ و معتبر

    و لعلم الله اللسان سبع عقور، و رائد للمنية صدوق، جعله مزموماً مخطوماً،
    و عن التصرف بذاته عاجزاً مقصوراً، فليس يجري ما لم يجره فارسه من العقل،
    و لا يفعل ما لم يأمره أميره من الرأي، و لم يجعله كالعين التي ترى ما
    نحاه اللاحظ بطرفة و ما لم ينحه، و كالأذن التي ما قصد السامع لاستماعه و
    ما لم يقصده. و كل ذلك لأنه صاحب الفتك و الإمضاء، و ينبوع الشر و البلاء،
    و كان إبراهيم بن أدهم الزاهد الناسك يطيل السكوت فليم في ذلك فقال:
    الكلام على أربعة وجوه: فمنه كلام ترجوه منفعته و تخشى عاقبته، فالفضل فيه
    السلامة منه، و كلام لا نرجو منفعته و لا تخشى عاقبته فأقل ما في تركه خفة
    المؤونة على بدنك و لسانك، و كلام لا ترجو منفعته و تخشى عاقبته، و هذا هو
    الداء العضال، و من الكلام كلام ترجو منفعته، و لا تخشى عاقبته، و هو الذي
    يجب عليك نشره، فإذا به قد بهرج ثلاثة أرباع الكلام، هذا على بلاغة
    إبراهيم بن أدهم و حكمته و كثرة معارضته أهل البيان، و هو الذي سأل عبد
    الله بن شبرمة بن طفيل الضبي قاضي أبي جعفر عن مسألة فأسرع الجواب فقال:
    تأن. فقال له ابن شبرمة: إذا سهل الطريق لم أحتشد و هذا نسيبه يروى لأياس
    بن معاوية مشهور، و في إبراهيم بن أدهم يقول محمد بن كناسة، و كان إبراهيم
    من أخواله بني عجل:

    رأيتك لا يكفيك ما دونه الغنى ... و قد كان يكفي دون ذلك أبن أدهما

    و كان يرى الدنيا صغيراً كبيرها ... و كان لحق الله فيها معظما

    و اكثر ما يلفى مع القوم صامتاً ... و إن قال بذّ القائلين و أحكما

    و محمد بن كناسة هذا شاعر محسن، و أهل النقد يستحسنون قوله:

    على حين أن شابت لداتي و لم أشب ... فمنها لحى مبيضة و قرون

    و ناصيت رأس الأربعين و أقبلت ... قساوة جني الشباب تلين

    و هو صاحب هذا الشعر و السائر:
    في انقباض و حشمة فإذا ... رأيت أهل الوفاء و الكرم

    أرسلت نفسي على سجيتها ... و قلت ما قلت، غير محتشم

    و من قوله:

    قعدت عن الإخوان عن غير ما قلى ... على غير نقصٍ في الإخاء و لا الود

    و لكن أيامي تخرمن مرتي ... فما أبلغ الحاجات إلا على جهد

    و من قوله يرثى حماداً الرواية - في أبيات معروفة:

    فهكذا يفسد الزمان و يف ... نى العلم فيه، و يذهب الأثر

    يرون أن ذلك منظور به قول عبد الله بن العباس و قد دلى زيد بن ثابت في
    قبره: من سره أن يرى كيف ذهاب العلم فهكذا ذهابه، و هو القائل لما أبغض
    زوجته و مر بجذع مصلوبٍ:

    أيا جذع مصلوب أتى دون صلبه ... ثلاثون حولا كاملاً هل تبادل؟

    فما أنت بالحمل الذي قد حملته ... بأعرض مني بالذي أنا حامل

    و كل هذا حسن، أو واقف بين الحسن و القبيح، فأما الذي يوافق هواي و يصنع
    في نفسيصنيع الواعظ الحسن في قلوب المخلصين، و ليس يستحق ذلك في حقيقة
    النقد إلا أني معجب به فقوله في نكبة أبي أيوب المورياني، و لعل استحساني
    إياه لموافقته شجناً في نفسي في هذا الوقت:

    لا ترى زاجراً لهمِّ القلوب ... كالرضا بالموكَّل المكتوبِ

    فاتق الله و ارضَ بالقصد حظاً ... لا تسيلنَّ في سبيل الذنوبِ

    لا يغرنَّك الذي غرَّ قوماً ... شربوا من حتوفهم بذنوببِ

    طلعت شمسهم عليهم نهاراً ... و أتتهم نحوسهم بغروبِ

    قد رأيت الذي أدالت و نالت ... وقعه الدهر من أبي أيوبِ

    و عدنا إلى كلام ابن أدهم، و قوا ابن أدهم في تزييف المنطق و المشورة
    بالصمت نسيب لقولهم: أبلغ الصمت ما يكون الكلام شراً منه، و كان حبيب بن
    أوس الطائي الشاعر يحكيعن أبي مسهر أحمد بن مروان الرملي النحوي قال: تكلم
    رجل في مجلس الهيثم بن صالح. فقال له الهيثم: يا هذا بكلام أمثالك رزق
    الصمت المحبه و قد أحسن الشاعر إذ قال:

    سأهجر ما يخاف علي منه ... و أترك ما هويت لما خشيتُ

    لسان المرء ينبي عن حجاه ... و عيُّ المرء يستره السكوتُ

    و مثل البيت الأول قول جامع هذا التعليق:

    أطعت العلى في هجر ليلى و إنني ... لأضمر فيها مثل ما يضمر الرندُ

    صريمة عزم لم يكن من رجالها ... سوى من العشاق قبل و لا بعدُ

    رأيت فراق النفس أهون ضيرةً ... عليَّ من الفعل الذي يكره المجدُ

    و في نحو من هذا الأقوال المخبرة عن التسلي قول المؤلف أيضاً:

    حبيبٌ ملكت الصبر بعد فراقه ... على أنني علقتهُ و الفتهُ

    محا حسن يأسي شخصه من تذكري ... فلو أنني لاقيته ما عرفتهُ

    و له أيضاً في نحو من ذلك، و لعله مُحَيَّنٌ بذكره، و ناطق بالشهادة على
    تقصيره و عجزه:

    و لاعبٍ بالهوى يؤمل أن يظ ... هر لي جفوة و أهواهُ

    قلت لقلبي و قد تتبعه: ... يا قلبُ إما أنا و أما هو!!

    و في المعنى الأول قال قائل مصيب: عيُّ صامتٍ خيرٌ من عيِّ ناطق. و من
    هاهنا قال بشار:

    و عيُّ القيام كعبيِّ الكلام ... و في الصمت عيِّ كعبيِّ القلم

    و في بعض الأحاديث إن ابن آدم إذا أصبح كفرت أعضاؤه للسان، و قالت: اتق
    الله فإنك إن استقمت استقمنا و إن اعوججت اعوججنا، و حدثني بعض شيوخنا
    بإسناد رفعه إلى أبي الدرداء أنه قال: لأن يعثر الرجل حتى يخر لوجهه خير
    له من أن يعثر بلسانه، و أنشدني هذا الشيخ في منظوم هذا المعنى لبعضهم:

    يموتُ الفتى من عَثرَة بلسانه ... و ليس يموت المرءُ من عَثرة الرجلِ

    فعثرته من فيه تُودي برأسهِ ... و عثرته بالرِّجل تُوسىَ على مهلِ
    و قال أبو الفضل الريعي - من ولد ربيعة بن الحارث بن عبد المطلب
    - حدثني محمد بن ذياد مؤدب المعتز - قال: بينا أنا جالس مع المعتز و
    الزَّبير بن بكار يقرأ عليه أخبار أبي السائب و كان المتوكل قال لي: إذا
    غضبت عليه فلا تضربه، و قل ليندون الخدم يضربه، قال: فترك المعتز الزبير،
    و قام يلعب مع الخدم، و قد كنت ظننت أنه قام لحاجة، فقلت ليندون: أما ترى
    إلى الأمير ترك شيخاً من قريش جالساً في سواده، و قام يلعب؟! لو كانت يدي
    مطلقةً عليه لأوجعته ضرباً، فقام إليه يندون ليضربه فهرب منه المعتز، فلم
    يزل يراوغه حتى سقط في عثرة، فدميت رجله، ففزعنا و بادرنا إليه و قال له
    الزبير: يا سيدي لو أعلمتني أنك قد ضجرت لقمت و لم أوذك فقال المعتز: لا
    بأس هوّن عليك و أنشد:

    يُصابُ الفتىَ من عَثرةٍ بلسانه ... و ليس يصابُ المرءُ من عثرة الرجلِ

    قال أبو الفضل: فحدثت بهذا الحديث أبا عبد الله بن حمدون فقال لي: أنا
    أحدثك عن أبيه المتوكل بنحوٍ من هذا، كنا عنده في اليوم الذي قتل في
    ليلته، فقام لحاجة، فلما رجع قمنا، و المنتصر جالس، فلما قرب منه قام،
    فنظر إليه المتوكل و قال له: اجلس يا محمد، و أقبل علينا فقال:

    هُم سَمَّنوا كَلباً ليأكل بعضَهُم ... و لو أخذوا بالحزم ما سمَّنوا كلباً

    قال أبو عبد الله: فوالله ما صلينا عتمة من تلك الليلة و المتوكل من أهل
    الدنيا.

    و نرجع إلى ما كنا فيه فنقول: قد كان يجب أن نقدم بين يدي هذا القول
    الرغبة الى الله في المعافاة من سورة الغضب و من غشية الأنف، و من البلوغ
    في طاعة الحنق إلى ما يردي الحلم، و إلي ما يهزم الرأي، و إلى ما يزين
    إطلاق اليد و اللسان بما يجنى منه الندم بعد ساعة فعله، و يوجد منه الأسف
    و اللهف غب يوم كونه، و قد ينجد على اماتة الغيظ و قتله، و ينفع في
    تَمصُّل الحقد و ذوبه أن تكون النفس كاملة وقوراً، و بأمور الزمن عروفاً،
    و ان يكن اللسان حديداً و القلب شديداً، فإن كمال الآلة يحبب الاقالة، و
    علوَّ القدرة يزين التكرم و الترفع عن المعاقبة، و قد قال قائل مجرب:

    كُنْ حاقداً ما دُمتَ ليتَ بقادر ... فإذا قَدِرْت فخلّ حقدكَ و اغفر

    و اعذُرْ أخاك إذا أساءَ فَرُبَما ... لجّتْ إساءتهُ إذا لم تَعذُرِ

    و كثيراً ما يقطع بالمخاطب عيه فيفزع إلى السّفه، يتل الغبيّ البئ حصره
    فيطلب إخفاء أمره بالشغب، و ذلك أخفض المواقف، و الأم الهزائم و قال علقمة
    بن علاثة في نحو من هذا المعنى: أول العيّ الاختلاط، و أسوء القول الإفراط
    هاهنا الاضطراب لشدة الغضب، فأما إذا أيقن أنه التام الحسنى، و الموفور
    للعلى فهو عند نفسه البحر الذي يلتهم كل ما ألقي فيه و هو ساج ساكن و
    الطود الذي يحتمل كل ما نيط به و هو قار ثابت فيعود طريقه إلى الاحتمال
    دميثاً، و عذره في الأعضاء - عند نفسه - واضحاً جميلاً.جعلنا الله ممن
    يقيم لطبيعته المعاذير في الأقصار عن الرذائل، كما يقيم الحجيج عليها في
    القصور عن الفضائل، و لا جعلنا ممن يمهد لنفسه العلل في هجران الخير
    لصعوبة طريقه. و يخرج لها الأسباب في إتيان الشر لإعتنانه، و اتفاق عروضه،
    و قد قال الشاعر:

    فلا تعذريني في الإساءة إنه ... شرار الرجال من يسيء فيعذر

    و تجزع نفس المرء من سب مرةٍ ... و تسمع ألفا مثلها ثم تصبر

    و قال آخر:

    و عذرك في القبائح مستتب ... و ليس الناس كلهم يلام

    و قال العبدي:

    عذرك عندي لك مبسوط ... و الذنب عن مثلك محطوط

    ليس بمسخوط فعال امرئٍ ... كل الذي يأتيه مسخوط

    و ينشد أصحاب المعاني:

    فخذ القليل من اللئيم و سبه ... إن اللئيم بما أتى معذور
    قالوا: و ليس هذا العذر في شيء، و إنما يريد أن اللئيم يسب بما
    يأتيه فيجعل وسما على وجهه كالعذار و قد يجوز أن يكون من العذر، و تأويله
    ذم اللئيم فإنه عند نفسه معذور، فذلك أوجب للوم عليه، لأنه لو استقصر فعله
    لدل على أنه أعطى قليلاً من نية مكثرة لو استطاعت.و قال المكتفي أبو محمد
    علي بن أحمد - لما كان كاتبه معه بالري أحمد بن أبي الأصبغ يكاتب عبد الله
    بن سليمان بن وهب بأخباره، فينهيها إلى أبيه المعتضد، فلما اجتمعا عاتبه
    المعتضد، قال فلم أزل أقارب و أباعد حتى صدقني عن المخبر له، و زال ما في
    نفسه، فقالت:

    و لما رأيت العذر يظلم وجهه ... و لم يبق إلا أن أقول فأكذبا

    خلطت بحق باطلا فتشبها ... و جئت من الباب الذي كان أصوبا

    و نحو من هذا قول علي بن الجهم بن بدر - مولى بني سامة بن لؤى و هو يدعا "
    ؟ " فيهم صميماً:

    و فتى ضاقت مذاهبه ... ضاق ذرعاً بالذي صنعا

    جعل الإقرار جنته ... حين رام العذر فامتنعا

    و قال آخر في ذم المعاذير، و نسبها إلى الأكاذيب:

    جد لي بغفرانك من قبل أن ... أخضع بالعذر و أن ارغبا

    فالعذر لا يسلم من زخرف ال ... كذب، و ما أنشط أن أكذبا

    و كتب إلي بعض عمال السلطان معتذراً من تأخر كتابه عني بأن قال: فإن رأيت
    أن تسامحني بما في غضون اعتذار المعتذر فعلت إن شاء الله تعالى. و قال
    قائل: زاد على هؤلاء كلهم في حسن القول و جودة المعنى:

    إياك و الأمر الذي إن توسعت ... موارده ضاقت عليك المصادر

    فلما حسن أن يعذر المرء نفسه ... و ليس من سائر الناس عاذر

    و أشعر منه و أحكم كعب الغنوي إذ يقول:

    لا تعذرن الدهر صاحب ريبةٍ ... فسان آتٍ أمر سوء، و عاذره

    و من هذا الجنس - و إن لم يكن من النوع نفسه - قول تميم بن أُبيَّ بن مقبل
    عوف بن حنيف بن العجلان بن عبد الله بن كعب بن ربيعة بن عامر بن صعصعة - و
    قد أحسن فيه ما شاء:

    يا حر أمسى سواد الرأس خالطه ... شيب القذال اختلاط الصفو بالكدر

    يا حر من يعتذر من أن بلم به ... ريب الزمان فإني غير معتذر

    فأما الكلام الذي يستحق قضية الحسن عندي، و لعله كذاك عند غيري فشعر
